/* FUNCTION : adcConvertVoltage
*
* DESCRIPTION : This function is used to convert the ADC 12 bit value to Voltage
* 				value ( 12 bit -> 4095) and 3.3V is maximum so the formula is
* 				voltage = adcValue * 3.3 / 4095
*
* PARAMETERS : uint32_t adcVal - ADC 12 bits value
* RETURNS 	 :  Float 		   - Voltage value
*
*/
float adcConvertVoltage(uint32_t adcVal)
{
	return adcVal * 3.3 / 4095;
}


/* FUNCTION : getTemp
*
* DESCRIPTION : This function is used to get the internal temperature of the Nucleo
* 				board, the formula to calculate is ((Vsense - V25)/AVG_SLOPE) + 25
* 				(refer STM32F411 reference manual p222)
* 				V25 and AVG_SLOPE value refer to STM32F411CE datasheet p120
*
* PARAMETERS :  float Vsense - Voltage value of temperature sensor
* RETURNS    :  Float 		 - Temperature value
*
*/
float getTemp(float Vsense)
{
	return ((Vsense - V25)/AVG_SLOPE) + 25;
}

/* FUNCTION : HAL_ADC_ConvCpltCallback
*
* DESCRIPTION : This function is used to get the ADC value and convert
* 				to Voltage value whenever conversion occur.
* 				Note: In order to change how often the callback occur change
* 				the adc_clock frequency or change the sampling time for
* 				each channel.
*
* PARAMETERS :  ADC_HandleTypeDef *hadc - ADC Handler
* RETURNS    :  Void
*
*/
void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef *hadc)
{
	// Get ADC value from DMA and convert the value to Voltage
	adc1Val = buffer[0];
	adc1Vol = adcConvertVoltage(adc1Val);

	adc2Val = buffer[1];
	adc2Vol = adcConvertVoltage(adc2Val);

	adc3Val = buffer[2];
	adc3Vol = adcConvertVoltage(adc3Val);

	tempVal = buffer[3];
	tempVol = adcConvertVoltage(tempVal);
}
